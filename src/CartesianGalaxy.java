/**
 * <h1>Cartesian Galaxy</h1>
 * Information for a Galaxy object in Cartesian coordinates.
 *
 * Created by pierfiedorowicz on 2/24/17.
 */
public class CartesianGalaxy {
    // Cartesian coordinates.
    double x,y,z;

    /**
     * Constructor
     *
     * @param x
     * @param y
     * @param z
     */
    public CartesianGalaxy(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
